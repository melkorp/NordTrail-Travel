// ─────────────────────────────────────────────────────────────
// generateStaticParams
// Говорит Next.js: «Создай HTML для каждого slug в ARTICLES».
// Новая статья появится автоматически — slug вручную не прописываешь.
// ─────────────────────────────────────────────────────────────
export function generateStaticParams() {
  return Object.keys(ARTICLES).map((slug) => ({ slug }));
}

// ─────────────────────────────────────────────────────────────
// generateMetadata
// ─────────────────────────────────────────────────────────────
export async function generateMetadata({
  params,
}: {
  params: Promise<{ slug: string }>;
}): Promise<Metadata> {
  const { slug } = await params;
  const article = ARTICLES[slug];

  if (!article) return { title: "Статья не найдена | NordTrail Travel" };

  return {
    title: `${article.title} | NordTrail Travel`,
    description: article.quickAnswer,
    alternates: {
      canonical: `/blog/${slug}/`,
    },
  };
}

// ─────────────────────────────────────────────────────────────
// СТРАНИЦА
// Серверный компонент: получает данные → передаёт в клиентский UI.
// ─────────────────────────────────────────────────────────────
export default async function BlogPostPage({
  params,
}: {
  params: Promise<{ slug: string }>; // В Next.js 15 params — Promise
}) {
  const { slug } = await params;
  const article = ARTICLES[slug];

  if (!article) {
    return (
      <main className="min-h-screen bg-bg flex items-center justify-center">
        <p className="text-text text-xl">Статья не найдена</p>
      </main>
    );
  }

  return <BlogPostClient article={article} />;
}
