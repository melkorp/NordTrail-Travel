// ─────────────────────────────────────────────────────────────
// generateStaticParams
// ─────────────────────────────────────────────────────────────
export function generateStaticParams() {
  return Object.keys(ITINERARY_DATA).map((slug) => ({ slug }));
}

// ─────────────────────────────────────────────────────────────
// generateMetadata
// ─────────────────────────────────────────────────────────────
export async function generateMetadata({
  params,
}: {
  params: Promise<{ slug: string }>;
}): Promise<Metadata> {
  const { slug } = await params;
  const data = ITINERARY_DATA[slug];

  if (!data) return { title: "Страница не найдена | NordTrail Travel" };

  return {
    title: `${data.h1} | NordTrail Travel`,
    description: data.quickAnswer.slice(0, 160),
    alternates: {
      canonical: `https://melkorp.github.io/NordTrail-Travel/destinations/${slug}/`,
    },
  };
}

// ─────────────────────────────────────────────────────────────
// Schema.org JSON-LD
// ─────────────────────────────────────────────────────────────
function JsonLd({ data }: { data: ItineraryData }) {
  const schema = {
    "@context": "https://schema.org",
    "@graph": [
      {
        "@type": "FAQPage",
        mainEntity: data.faq.map((item) => ({
          "@type": "Question",
          name: item.q,
          acceptedAnswer: {
            "@type": "Answer",
            text: item.a,
          },
        })),
      },
      {
        "@type": "TouristTrip",
        name: data.h1,
        description: data.quickAnswer,
        touristType: "Adventure",
        itinerary: data.days.map((d) => ({
          "@type": "TouristAttraction",
          name: d.title,
          description: d.content,
        })),
      },
    ],
  };

  return (
    <script
      type="application/ld+json"
      dangerouslySetInnerHTML={{ __html: JSON.stringify(schema) }}
    />
  );
}

