// ─────────────────────────────────────────────────────────────
// generateMetadata
// ─────────────────────────────────────────────────────────────
export async function generateMetadata({
  params,
}: {
  params: Promise<{ slug: string }>;
}): Promise<Metadata> {
  const { slug } = await params;
  const data = BEST_TIME[slug];

  if (!data) return { title: "Страница не найдена | NordTrail Travel" };

  return {
    title: `${data.h1} | NordTrail Travel`,
    description: data.quickAnswer.slice(0, 160),
    alternates: {
      canonical: `/destinations/${slug}/best-time/`,
    },
  };
}

// ─────────────────────────────────────────────────────────────
// Schema.org JSON-LD
// ─────────────────────────────────────────────────────────────
function JsonLd({ data }: { data: BestTimeData }) {
  const schema = {
    "@context": "https://schema.org",
    "@graph": [
      {
        "@type": "FAQPage",
        mainEntity: data.faq.map((item) => ({
          "@type": "Question",
          name: item.q,
          acceptedAnswer: {
            "@type": "Answer",
            text: item.a,
          },
        })),
      },
      {
        "@type": "TouristAttraction",
        name: data.destinationName,
        description: data.quickAnswer,
        url: `https://nordtrail.travel/destinations/${data.destinationSlug}/`,
      },
    ],
  };

  return (
    <script
      type="application/ld+json"
      dangerouslySetInnerHTML={{ __html: JSON.stringify(schema) }}
    />
  );
}

